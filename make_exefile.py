# 실행 파일에 필요한 패키지(pyinstaller)를 설치
# pip install pyinstaller
# cmd창에 명령어(pyinstaller (경로)/파일명.py)를 작성하여 실행파일 만들기
# 실행도 cmd창에서 실행되어야 함
# 하나의 exe파일로 만들고 싶을 경우 추가 명령어(-F) 필요
# -> pyinstaller -F (경로)/파일명.py
# cmd창을 이용하지 않고 바로 실행시키고 싶을 경우 추가 명령어(-w) 필요
# -> pyinstaller -w (경로)/파일명.py

# 하나의 exe파일로만 바로 실행시키기 위하여 다음과 같이 cmd창에 입력
